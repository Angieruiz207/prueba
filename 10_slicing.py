nombre = 'Cesar Quintero'
# INDEXING - INDEXACIÓN
# Posición índice de 'C' es cero:
print (nombre[0])
print (nombre[4])
#SLICING: VER UNA FRACCIÓN DE LOS VALORES
print (nombre[1:4])
print (nombre[2:7])
print (nombre[7:13])
#CsrQ
print(nombre [0:6:1])
print (nombre[0:])
print(nombre [0:13:3])
# Regresivo
print(nombre [-14:-1])
print(nombre [-1:-9:-1])
print(nombre [-1::-1])
